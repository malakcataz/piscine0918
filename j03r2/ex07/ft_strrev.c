char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	int		a;

	a = 0;
	i = 0;
	while (str[i] != '\0')
		i++;
	i = i - 1;
	j = 0;
	while (j <= i / 2)
	{
		a = str[i - j];
		str[i - j] = str[j];
		str[j] = a;
		j++;
	}
	return (str);
}
