#include <unistd.h>

int		str_count(char **argv)
{
	int i;
	int j;
	int res;

	i = 1;
	res = 0;
	while (i < 10)
	{
		j = 0;
		while (argv[i][j] != '\0')
		{
			if ((argv[i][j] < '1' || argv[i][j] > '9') && argv[i][j] != '.')
				res = res + 1;
			j++;
		}
		if (j != 9 || res > 0)
		{
			write(1, "Error\n", 6);
			return (0);
		}
		i++;
	}
	return (1);
}
