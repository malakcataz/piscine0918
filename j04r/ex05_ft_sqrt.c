int		ft_sqrt(int nb)
{
	int		i;

	i = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (i * i < nb)
	{
		i++;
		if (i > 46340)
			return (0);
	}
	if (i * i > nb)
		return (0);
	else
		return (i);
}
