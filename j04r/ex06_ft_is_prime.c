int		ft_is_prime(int nb)
{
	int		i;

	if (nb > 1)
	{
		i = 2;
		while (i <= (nb / i))
		{
			if ((nb % i) != 0)
				i++;
			else
				return (0);
		}
		return (1);
	}
	else
		return (0);
}
